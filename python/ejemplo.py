#!/usr/bin/python3

# Los strings se pueden definir con " o '
print("Hola a todos")


# Los strings multilinea se definen con """ o '''
print(
"""
	Hola a todos
	Hola a todos 2
	chao a todos
"""
)


# Concatenamos o unimos strings con el operador +
# \n significa salto de linea
print("Hola a todos \n " + "Chao a todos" )
